package shift;

public class Shift {

    private static int a = 100;
    private static int b = 99;
    private static int c = 98;

    public static void main(String[] arg){
        System.out.println("100 >> 1 = " + (a >> 1));
        System.out.println("99 >> 1 = " + (b>>1));
        System.out.println("98 >> 1 = " + (c>>1));
    }

}
